	var x, y IntSet
	x.Add(2)
	x.Add(144)
	x.Add(52)
	x.Add(9)
	fmt.Println("x:", x.String())

	y.Add(1)
	y.Add(5)
	y.Add(144)
	y.Add(34)
	fmt.Println("y:", y.String())

